// MessageReference contains reference data sent with crossposted [Message]s.
type MessageReference struct {
	Type            types.MessageReference `json:"type,omitempty"`
	MessageID       string                 `json:"message_id"`
	ChannelID       string                 `json:"channel_id,omitempty"`
	GuildID         string                 `json:"guild_id,omitempty"`
	FailIfNotExists *bool                  `json:"fail_if_not_exists,omitempty"`
}

func (m *Message) reference(refType types.MessageReference, failIfNotExists bool) *MessageReference {
	return &MessageReference{
		Type:            refType,
		GuildID:         m.GuildID,
		ChannelID:       m.ChannelID,
		MessageID:       m.ID,
		FailIfNotExists: &failIfNotExists,
	}
}

// Reference returns a MessageReference of the given [Message].
func (m *Message) Reference() *MessageReference {
	return m.reference(types.MessageReferenceDefault, true)
}

// SoftReference returns a MessageReference of the given [Message].
// If the message doesn't exist it will instead be sent as a non-reply [Message].
func (m *Message) SoftReference() *MessageReference {
	return m.reference(types.MessageReferenceDefault, false)
}

// Forward returns a MessageReference for a forwarded [Message].
func (m *Message) Forward() *MessageReference {
	return m.reference(types.MessageReferenceForward, true)
}

// ContentWithMentionsReplaced will replace all @<id> mentions with the username of the mention.
func (m *Message) ContentWithMentionsReplaced() (content string) {
	content = m.Content

	for _, u := range m.Mentions {
		content = strings.NewReplacer(
			"<@"+u.ID+">", "@"+u.Username,
			"<@!"+u.ID+">", "@"+u.Username,
		).Replace(content)
	}
	return
}

