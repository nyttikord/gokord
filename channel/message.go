// A Message stores all data related to a specific Discord message.
type Message struct {
	ID        string `json:"id"`
	ChannelID string `json:"channel_id"`
	GuildID   string `json:"guild_id,omitempty"`
	// Content of the [Message].
	Content string `json:"content"`
	// The time at which the last edit of the [Message] occurred, if it has been edited.
	EditedTimestamp *time.Time `json:"edited_timestamp"`
	// The roles mentioned in the [Message].
	MentionRoles []string `json:"mention_roles"`
	// Whether the [Message] is text-to-speech.
	TTS bool `json:"tts"`
	// Whether the [Message] mentions everyone.
	MentionEveryone bool `json:"mention_everyone"`
	// The author of the [Message].
	// This is not guaranteed to be a valid user.User (webhook-sent messages do not possess a full author).
	Author *user.User `json:"author"`
	// A list of [MessageAttachment]s present in the [Message].
	Attachments []*MessageAttachment `json:"attachments"`
	// A list of [component.Message] attached to the [Message].
	Components []component.Message `json:"-"`
	// A list of [MessageEmbed] present in the [Message].
	Embeds []*MessageEmbed `json:"embeds"`
	// A list of [user.User]s mentioned in the [Message].
	Mentions []*user.User `json:"mentions"`
	// A list of [Reactions] to the [Message].
	Reactions []*Reactions `json:"reactions"`
	// Whether the [Message] is pinned or not.
	Pinned bool `json:"pinned"`
	// Type of the [Message].
	Type types.Message `json:"type"`
	// [Webhook.ID] of the [Message], if it was generated by a [Webhook].
	WebhookID string `json:"webhook_id"`
	// Member properties for this message's author, contains only partial information.
	Member *user.Member `json:"member"`
	// [channel.Channel]s specifically mentioned in this [Message].
	//
	// Not all channel mentions in a message will appear.
	// Only textual channels that are visible to everyone in a lurkable guild.Guild will be included.
	// Only crossposted messages (via Channel Following) currently include this at all.
	MentionChannels []*Channel `json:"mention_channels"`
	// Is sent with Rich Presence-related chat embeds.
	Activity *MessageActivity `json:"activity"`
	// Is sent with Rich Presence-related chat embeds.
	Application *MessageApplication `json:"application"`
	// MessageReference contains [MessageReference] data sent with crossposted or reply [Message]s.
	// This does not contain the reference *to* this Message;
	// this is for when *this* Message references another.
	//
	// To generate a reference to this message, use [Message.Reference].
	MessageReference *MessageReference `json:"message_reference"`
	// ReferencedMessage associated with the [MessageReference].
	//
	// NOTE: This field is only returned for messages with a type [types.MessageReply] or
	// [types.MessageThreadStarterMessage].
	// If the message is a reply but the ReferencedMessage field is not present,
	// the backend did not attempt to fetch the message that was being replied to, so its state is unknown.
	// If the field exists but is null, the referenced message was deleted.
	ReferencedMessage *Message `json:"referenced_message"`
	// The message associated with the [MessageReference].
	// This is a minimal subset of fields in a [Message] (e.g. Author is excluded)
	//
	// NOTE: This field is only returned when referenced when [MessageReference.Type] is
	// [types.MessageReferenceForward].
	MessageSnapshots []MessageSnapshot `json:"message_snapshots"`
	// InteractionMetadata is sent when the [Message] is a response to an [interaction.Interaction], without an existing
	// [Message].
	// This means responses to [Message] component interactions do not include this property,
	// instead including a [MessageReference], as components exist on preexisting messages.
	InteractionMetadata *MessageInteractionMetadata `json:"interaction_metadata"`
	// Flags of the [Message], which describe extra features of a [Message].
	Flags MessageFlags `json:"flags"`
	// Thread that was started from this [Message], includes thread member object.
	Thread *Channel `json:"thread,omitempty"`
	// StickerItems represents sent [emoji.Sticker]s, if there were any.
	StickerItems []*emoji.StickerItem `json:"sticker_items"`
	// Poll contained in the [Message].
	Poll *Poll `json:"poll"`
}

// GetCustomEmojis pulls out all the custom (Non-unicode) emojis from a [Message] and returns them.
func (m *Message) GetCustomEmojis() []*emoji.Emoji {
	var toReturn []*emoji.Emoji
	emojis := emoji.Regex.FindAllString(m.Content, -1)
	if len(emojis) < 1 {
		return toReturn
	}
	for _, em := range emojis {
		parts := strings.Split(em, ":")
		toReturn = append(toReturn, &emoji.Emoji{
			ID:       parts[2][:len(parts[2])-1],
			Name:     parts[1],
			Animated: strings.HasPrefix(em, "<a:"),
		})
	}
	return toReturn
}

// MessageFlags is the flags of [Message].
// https://discord.com/developers/docs/resources/channel#message-object-message-flags
type MessageFlags int

const (
	// MessageFlagsCrossPosted has been published to subscribed channels (via Channel Following).
	MessageFlagsCrossPosted MessageFlags = 1 << 0
	// MessageFlagsIsCrossPosted is originated from a message in another channel (via Channel Following).
	MessageFlagsIsCrossPosted MessageFlags = 1 << 1
	// MessageFlagsSuppressEmbeds does not include any embeds when serializing this message.
	MessageFlagsSuppressEmbeds MessageFlags = 1 << 2
	// MessageFlagsSourceMessageDeleted (via Channel Following).
	MessageFlagsSourceMessageDeleted MessageFlags = 1 << 3
	// MessageFlagsUrgent came from the urgent message system (official).
	MessageFlagsUrgent MessageFlags = 1 << 4
	// MessageFlagsHasThread with the same id as the message.
	MessageFlagsHasThread MessageFlags = 1 << 5
	// MessageFlagsEphemeral is only visible to the user who invoked the Interaction.
	MessageFlagsEphemeral MessageFlags = 1 << 6
	// MessageFlagsLoading is an Interaction Response and the bot is "thinking".
	MessageFlagsLoading MessageFlags = 1 << 7
	// MessageFlagsFailedToMentionSomeRolesInThread and add their members to the thread.
	MessageFlagsFailedToMentionSomeRolesInThread MessageFlags = 1 << 8
	// MessageFlagsSuppressNotifications will not trigger push and desktop notifications.
	MessageFlagsSuppressNotifications MessageFlags = 1 << 12
	MessageFlagsIsVoiceMessage        MessageFlags = 1 << 13
	// MessageFlagsHasSnapshot (via Message Forwarding)
	MessageFlagsHasSnapshot = 1 << 14
	// MessageFlagsIsComponentsV2 this message uses the new components system.
	// Disables the ability of sending `content` and `embeds`
	MessageFlagsIsComponentsV2 MessageFlags = 1 << 15
)

